class Solution:
	# @param A : list of integers
	# @param B : list of integers
	# @param C : list of integers
	# @return an integer
    def solve(self, A, B, C):
        i, j, k = 0, 0, 0
        result = None
        while i< len(A) and j < len(B) and k < len(C):
            if not result:
                result = max(A[i],B[j],C[k]) - min(A[i],B[j],C[k])
            else:
                temp = max(A[i],B[j],C[k]) - min(A[i],B[j],C[k])
                if temp == 0:
                    return 0
                result = temp if temp<result else result
                print(A[i],B[j],C[k]," => ",temp)
            if A[i] < B[j] or A[i] < C[k]:
                i += 1
            if i<len(A) and (B[j] < A[i] or B[j] < C[k]):
                j += 1
            else:
                k += 1
        return result


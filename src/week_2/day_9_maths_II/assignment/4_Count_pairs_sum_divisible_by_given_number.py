class Solution:
    # @param A : list of integers
    # @param B : integer
    # @return an integer
    def solve(self, A, B):
        if len(A) < 2:
            return 0
        remainder_map = {i:0 for i in range(B)}
        for i in A:
            remainder_map[i % B] += 1
        ans = 0
        if remainder_map[0]>1:
            ans += (remainder_map[0] * (remainder_map[0] - 1)) // 2

        i = 1
        while i < B // 2:
            ans += remainder_map[i] * remainder_map[B - i]
            i += 1
            while i < B // 2 and (remainder_map[i] == 0 or remainder_map[B - i] == 0):
                i += 1

        if B%2 ==0:
            if remainder_map[i] > 1:
                ans += (remainder_map[i] * (remainder_map[i] - 1)) // 2
        else:
            ans += remainder_map[i] * remainder_map[B - i]
        return ans


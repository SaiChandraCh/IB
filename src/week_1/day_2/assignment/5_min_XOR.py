class Solution:
    # @param A : list of integers
    # @return an integer
    def findMinXor(self, A):
        A = sorted(A)
        min = A[0]^A[1]
        length = len(A)
        for i in range(2, length):
            if A[i] ^ A[i - 1] < min:
                min = A[i] ^ A[i - 1]
        return min


